
public class FeatureIDAndValue {

	int id;
	double value;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public double getValue() {
		return value;
	}
	public void setValue(double value) {
		this.value = value;
	}
	public FeatureIDAndValue(int id, double value) {
		this.id = id;
		this.value = value;
	}
	
}
